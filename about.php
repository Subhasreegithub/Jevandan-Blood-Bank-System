 <h2 class="highlight-heading">About Jevandan</h2>
 <p style="font-size: 20px; " >
  Jevandan is an intelligent and easy-to-use Blood Bank Management System created to bridge the gap between blood donors and those in urgent need. It provides a centralized platform where users can register as donors, recipients, or organize blood donation camps with just a few clicks. The system ensures real-time tracking and management of blood availability in registered blood banks, making the process fast, efficient, and transparent.

Our goal is to simplify the way blood donation works and encourage more people to become donors by making the process accessible and organized. Whether it’s scheduling a donation camp, finding a nearby donor, or managing a request, Jevandan helps streamline every step — saving time and most importantly, saving lives. With technology and community support, Jevandan aims to build a stronger, more responsive healthcare ecosystem.  
